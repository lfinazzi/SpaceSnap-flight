/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  ret = HAL_OK;										// Saves return of HAL calls
  state = 0;										// state = IDLE
  picture_delay_start = 0;							// Moment the delayed photo instruction was executed
  picture_delay_mins = 0;							// Amount of 5-minute intervals to take a delayed photo
  delayed_flag = 0;									// Flag used to transmit scheduled command buffer for CMD_TakeDelayedPicture() only once

  ignore_flag = 0;

  uint32_t timeout_start = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  __enable_irq();
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_UART4_Init();
  MX_DCMI_Init();
  MX_USART1_UART_Init();
  MX_I2C2_Init();
  MX_SPI2_Init();
  MX_FSMC_Init();
  MX_TIM11_Init();
  MX_IWDG_Init();
  /* USER CODE BEGIN 2 */
  Log("UNSAM SpaceSnap initializing...\r\n");
  HAL_UARTEx_ReceiveToIdle_IT(&huart1, (uint8_t*) rx_buffer, AIRMAC_SIZE+1);		// Arms UART1 for IT reception
  AssignSRAMMemory();																// Assigns SRAM pointers
  GPIO_Init();

  TestFRAM();																		// Tests integrity of FRAM
  LoadBoardStatusFRAM();															// Loads board status saved in FRAM

  LogBoardStatus();																	// Logs current status to UART4 (debug)

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  Log("---------------------------------------------------\r\n");
  while (1)
  {
	  HAL_IWDG_Refresh(&hiwdg); 													// Kick the IWDG once per loop
	  switch (state) {
		  case STATE_IDLE:
			  if(rx_flag){
				  HandleIncomingCommand(STATE_IGNORE);								// Waits for command reception and modifies state accordingly
				  // If USS command, go to STATE_EXECUTE_COMMAND
				  // If other subsystem command, go to STATE_IGNORE
			  }
			  break;

		  case STATE_IGNORE:														// TODO: Might be a bug where RS485 auto-resets.

			  if(ignore_flag == 0)													// Only transmit buffer first time this is executed
			  {
				  Log("Waiting for reset\r\n");										// Return for DEB-UART for user to know IGNORE was reached
				  DisableRS485();
				  timeout_start = HAL_GetTick();									// Register starting time
				  ignore_flag = 1;
			  }

			  // Waiting for HW reset from LS02 - Using non-blocking (no while) - or timeout
			  if(PollUSSReset() || HAL_GetTick() - timeout_start >= (uint32_t)IGNORE_TIMEOUT_MAX){    // GPIO reset high --> reactivate reception (or timeout)
				  Log("Leaving IGNORE mode.\r\n");
				  Log("---------------------------------------------------\r\n");
				  rx_flag = 0; 															// to avoid receiving something while blocked
				  state = STATE_IDLE;
				  ignore_flag = 0;
				  EnableListenRS485();
			  }
			  break;

		  case STATE_EXECUTE_COMMAND:
			  current_command_pointer = GetCommand(instr_number);					// Identifies command to execute
			  cmd_ret = ExecuteCommand(current_command_pointer, instr_opcode);		// Executes command with opcode
			  if (cmd_ret == CMD_SCHEDULED)
				  state = STATE_DELAYED_PICTURE;
			  else
				  state = STATE_TRANSMIT_RESPONSE;
			  break;

		  // **************************************************************************************************************************************************
		  // Only the CMD_TakePictureDelayed() can make the program reach this state
		  case STATE_DELAYED_PICTURE:
			  if(delayed_flag == 0)									// Only transmit buffer first time this is executed
			  {
				  TransmitBufferRS485();							// Return for OBC, indicating command scheduled if everything ok
				  delayed_flag = 1;
			  }

			  if (rx_flag) {										// Board is catching any incoming messages to cancel the delayed picture command
				  HandleIncomingCommand(STATE_DELAYED_PICTURE);		// If other subsystem receives a message, ignore but stay in STATE_DELAY_PICTURE
				  break;
			  }

			  else if (HAL_GetTick() - picture_delay_start >= (uint32_t)picture_delay_mins * MIN_INTERVAL * 60 * 1000)		// Has the delay passed?
			  {
				  current_command_pointer = GetCommand(CMD_TAKE_PICTURE_ID);				// CMD_TakePicture()
				  cmd_ret = ExecuteCommand(current_command_pointer, instr_opcode);			// Take a picture

				  ResetLS02();									// Re-activates LS-02 for next incoming instruction

				  Log("Going IDLE...\r\n");
				  state = STATE_IDLE;							// No point in transmitting response, as satellite will not be over GS
				  delayed_flag = 0;
			  }
			  break;
		  // **************************************************************************************************************************************************

		  case STATE_TRANSMIT_RESPONSE:
			  Log("Transmitting buffer\r\n");
			  Log("---------------------------------------------------\r\n");
			  TransmitBufferRS485();			// Transmits tx_buffer
			  ResetLS02();						// Re-activates LS-02 for next incoming instruction
			  state = STATE_IDLE;
			  break;

		  default:
			  state = STATE_IDLE;
			  break;
	}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	UpdateStatus();					// TODO: updates uptime_ms for now, add accordingly
	SaveBoardStatusFRAM();			// TODO: Saves the status to FRAM, is this too slow to save on every iteration?
  }
  /* USER CODE END 3 */
}

